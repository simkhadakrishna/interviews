package composition3;

public class Ceiling {
	private int height;
	private String paintedColor;
	public Ceiling(int height, String paintedColor) {
		super();
		this.height = height;
		this.paintedColor = paintedColor;
	}
	public int getHeight() {
		return height;
	}
	public String getPaintedColor() {
		return paintedColor;
	}
	

}
